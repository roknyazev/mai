function Result = right_parts(t, K)
Kf = 0.2506628274631;
Tf = 0.1;
K1 = 10;
T1 = 0.005;
K3 = 1;
T3 = 0.05;
xi = 0.07;
K4 = 3;
T4 = 0.01;
Klin = 0.11239753;

cov = [K(1) K(2) K(3) K(4) K(5);
       K(6) K(7) K(8) K(9) K(10);
       K(11) K(12) K(13) K(14) K(15);
       K(16) K(17) K(18) K(19) K(20);
       K(21) K(22) K(23) K(24) K(25)];

A = [-1/Tf 0 0 0 0;
    K1/T1 -1/T1 0 0 -K1/T1;
    0 0 0 1 0;
    0 (K3*Klin)/(T3*T3) -1/(T3*T3) -(2*xi)/T3 0;
    0 (T4*K4*K3*Klin)/(T3*T3) -(T4*K4)/(T3*T3) K4-(2*T4*K4*xi/T3) 0];

B = [Kf/Tf; 0; 0; 0; 0];

L = 1;

fK = A * cov + cov * A' + B * L * B';
Result = [fK(1, 1); fK(1, 2); fK(1, 3); fK(1, 4); fK(1, 5);
          fK(2, 1); fK(2, 2); fK(2, 3); fK(2, 4); fK(2, 5);
          fK(3, 1); fK(3, 2); fK(3, 3); fK(3, 4); fK(3, 5);
          fK(4, 1); fK(4, 2); fK(4, 3); fK(4, 4); fK(4, 5);
          fK(5, 1); fK(5, 2); fK(5, 3); fK(5, 4); fK(5, 5)];
end